# lets understand strings in python

# A string is a sequence of characters enclosed within single quotes (' ') or double quotes (" ")


chaai = 'masla chai'


# get first character
print(chaai[0])

# get masla in the string : we use slicing concept here

chaai_masla = chaai[0:5]  # it will get character from index 0 to index 4 and by default step is 1

# step is optional is used to skip characters

print(chaai_masla) # masla

# slicing [:start_index : end_index : step] if i give [:] it will return full string








